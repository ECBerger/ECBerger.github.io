function findTriNeighbors(triIds) 
{
    // create common edges

    var edges = [];
    var numTris = triIds.length / 3;

    for (var i = 0; i < numTris; i++) {
        for (var j = 0; j < 3; j++) {
            var id0 = triIds[3 * i + j];
            var id1 = triIds[3 * i + (j + 1) % 3];
            edges.push({
                id0 : Math.min(id0, id1), 
                id1 : Math.max(id0, id1), 
                edgeNr : 3 * i + j
            });
        }
    }

    // sort so common edges are next to each other

    edges.sort((a, b) => ((a.id0 < b.id0) || (a.id0 == b.id0 && a.id1 < b.id1)) ? -1 : 1);

    // find matching edges

    var neighbors = new Float32Array(3 * numTris);
    neighbors.fill(-1);		// open edge

    var nr = 0;
    while (nr < edges.length) {
        var e0 = edges[nr];
        nr++;
        if (nr < edges.length) {
            var e1 = edges[nr];
            if (e0.id0 == e1.id0 && e0.id1 == e1.id1) {
                neighbors[e0.edgeNr] = e1.edgeNr;
                neighbors[e1.edgeNr] = e0.edgeNr;
            }
            nr++;
        }
    }

    return neighbors;
}

// ------------------------------------------------------------------
function calculateAverageSpacing(vertices, faceTriIds) {
    let totalDistance = 0;
    let edgeCount = 0;
    
    const numTris = faceTriIds.length / 3;
    
    for (let i = 0; i < numTris; i++) {
        for (let j = 0; j < 3; j++) {
            const id0 = faceTriIds[3 * i + j];
            const id1 = faceTriIds[3 * i + (j + 1) % 3];
            
            const x0 = vertices[3 * id0];
            const y0 = vertices[3 * id0 + 1];
            const z0 = vertices[3 * id0 + 2];
            
            const x1 = vertices[3 * id1];
            const y1 = vertices[3 * id1 + 1];
            const z1 = vertices[3 * id1 + 2];
            
            const dx = x1 - x0;
            const dy = y1 - y0;
            const dz = z1 - z0;
            
            const distance = Math.sqrt(dx * dx + dy * dy + dz * dz);
            totalDistance += distance;
            edgeCount++;
        }
    }
    
    const averageSpacing = totalDistance / edgeCount;
    return Number(averageSpacing.toPrecision(3));
}

